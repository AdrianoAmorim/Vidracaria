    private ArrayList<Cargo> pesquisarCargoCaretUpdate(JTextField campo) {
        CargoCRUD cargoCRUD = new CargoCRUD();
        ArrayList<Cargo> listaCargos = new ArrayList<>();

        if (!campo.getText().isEmpty()) {
            listaCargos = cargoCRUD.consultarCargo(tfBuscaCargoCodigo, tfBuscaCargoDescricao);
        }
        return listaCargos;
    }

    private void tfBuscaCargoDescricaoCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_tfBuscaCargoDescricaoCaretUpdate
        DefaultListModel listModelo = new DefaultListModel();
        jlBuscaCargoResultConsulta.setModel(listModelo);

        listModelo.removeAllElements();

        if (!tfBuscaCargoDescricao.getText().isEmpty()) {
            for (Cargo cargo : this.pesquisarCargoCaretUpdate(tfBuscaCargoDescricao)) {
                listModelo.addElement(cargo.getDescricaoCargo());
            }
        }
    }//GEN-LAST:event_tfBuscaCargoDescricaoCaretUpdate

    private void tfBuscaCargoCodigoCaretUpdate(javax.swing.event.CaretEvent evt) {//GEN-FIRST:event_tfBuscaCargoCodigoCaretUpdate
        DefaultListModel listModelo = new DefaultListModel();
        jlBuscaCargoResultConsulta.setModel(listModelo);

        listModelo.removeAllElements();

        if (!tfBuscaCargoCodigo.getText().isEmpty()) {
            for (Cargo cargo : this.pesquisarCargoCaretUpdate(tfBuscaCargoCodigo)) {
                listModelo.addElement(cargo.getDescricaoCargo());
            }
        }
    }//GEN-LAST:event_tfBuscaCargoCodigoCaretUpdate

    private void jlBuscaCargoResultConsultaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jlBuscaCargoResultConsultaMouseClicked
        if (evt.getClickCount() == 2) {
            CargoCRUD cargoCRUD = new CargoCRUD();

            this.cargo = cargoCRUD.consultarCargo(tfBuscaCargoCodigo, tfBuscaCargoDescricao).get(0);
            this.dispose();
        }
    }//GEN-LAST:event_jlBuscaCargoResultConsultaMouseClicked

